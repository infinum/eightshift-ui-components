import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { glob } from 'glob';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, '..');
const iconsDir = path.join(rootDir, 'lib/icons/ui-icons');
const outputFile = path.join(rootDir, 'lib/icons/generated-icon-loaders.js');

const normalizeIconName = (name) => name.replace(/[-_]+([a-z0-9])/gi, (_, char) => char.toUpperCase()).replace(/^([A-Z])/, (char) => char.toLowerCase());

const iconFiles = (await glob('*.jsx', { cwd: iconsDir })).sort();

const fileContents = `${[
	'// This file is auto-generated by scripts/generate-icon-loaders.mjs.',
	'// Do not edit it manually.',
	'',
	'export const iconLoaders = {',
	...iconFiles.map((file) => {
		const exportName = normalizeIconName(file.replace(/\.jsx$/, ''));
		const importPath = `./ui-icons/${file}`;

		return `\t${exportName}: () => import('${importPath}').then((module) => module.${exportName} ?? null),`;
	}),
	'};',
	'',
	'export const loadIconByName = (name) => iconLoaders[name]?.() ?? Promise.resolve(null);',
	'export const hasIconLoader = (name) => Object.hasOwn(iconLoaders, name);',
	'',
].join('\n')}`;

const existingContents = await fs.readFile(outputFile, 'utf8').catch(() => null);

if (existingContents !== fileContents) {
	await fs.writeFile(outputFile, fileContents);
}
